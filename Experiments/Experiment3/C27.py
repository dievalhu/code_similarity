import numpy as np ## Python numerico

B = 15 #Numero de cubetas
X = [20,35,10,40,25,30,12,8,95,47,51,15] #Elementos
T = [None] * B #Tama√±o tabla hash

for i in range(len(X)):#Recorrer el numero de elementos
  H = X[i] % B #Funcion Hash
  if T[H] == None:
    T[H] = X[i]#guardar en el espacio vacio
  else: # Tratamiento de colisiones
    k = (X[i] % (B - 1)) + 1 #REDISPERCION
    while T[H] != None:
      H = (H + k) % B #REDISPERCION
    T[H] = X[i]#guardar en el espacio vacio

print(T)

