lista = [23,14,9,6,30,12,18]
def double_hashing(valores, tamTabla):
    tablaHash = [None] * tamTabla #Creaci√≥n tabla hash
    for i in range(len(valores)):
        hash1 = valores[i] % tamTabla # Primer hash - FORMULA: xMODB
        if tablaHash[hash1] is None:
            tablaHash[hash1] = valores[i] #Asignar valor de la lista en la posicion del hash1
        else:
            hash2 = hash1 # Cambio de valores para Hash2
            while tablaHash[hash2] is not None:
                k_x = (valores[i]%(tamTabla-1))+1
                hash2 = (hash2 + k_x) % tamTabla 
            tablaHash[hash2] = valores[i] #Asignar valor de la lista en la posicion del hash2
    return tablaHash 
print( double_hashing(lista, 10))
